//Função Soma
float soma(int num1, int num2) {
	return(num1+num2);
}
//================================

//Função Subtração
float subt(int num1, int num2) {
	return(num1-num2);
}
//================================

//Função Multiplicação
float multi(int num1, int num2) {
	return(num1*num2);
}
//================================

//Função Divisão
float divi(int num1, int num2) {
	return(num1/num2);
}
//================================

//Função Multiplicação de tres números
int multi(int num1, int num2, int num3) {
	return(num1*num2*num3);
}
//================================

//Função Subtração de tres números
int subt(int num1, int num2, int num3) {
	return(num1-num2-num3);
}